package cs445program1;

/**
 * This Point class represents a point in a coordinate system
 * Has x and y values as float
 * 
 * @author shun7817
 */
public class Point {
    public float x;
    public float y;
/**
 * A Point constructor that initializes Point object with x and y.
 */
    public Point(float x, float y) {
        this.x = x;
        this.y = y;
    }
}
